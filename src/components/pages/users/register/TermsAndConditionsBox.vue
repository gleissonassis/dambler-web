    <p>9.13.1. Produtos Devolvidos ao Escritório de Entrega</p>
    <p>As diferentes transportadoras vão guardar o Produto por diferentes períodos de tempo! O receptor deverá contactar a transportadora imediatamente!</p>

    <p>Caso o receptor não receba o Produto da transportadora, dentro do período acordado, então o Produto será devolvido ao armazém da Empresa. Nas raríssimas vezes em que isso acontecer, a Empresa contactará o receptor, para que os dois decidam uma maneira de reagendar a entrega!</p>

    <p>A partir da data em que o Produto for devolvido, sob esta seção, o Usuário Registrado deverá aceitar o Produto no prazo de 28 dias. Caso ele não seja capaz disso, então a Empresa cancelará o pedido, e o Produto será readquirido pela Empresa.</p>

