    <p>Seção 5. O Processo de Licitação</p>
    <p>5.1. Licitar</p>
    <p>Licitar num leilão só pode ser realizado por um Usuário Registrado do Website. Quando um Usuário Registrado licita num Leilão, gasta um número de créditos.</p>

    <p>5.2. Horário dos Leilões</p>
    <p>Cada leilão tem um horário dedicado que é exibido na informação relativa a esse Leilão. Quando um leilão atinge o segundo final do seu dia, ele vai ser colocado em Pausa, e será retomado novamente no primeiro segundo do seu horário do dia seguinte.</p>

    <p>5.3. Créditos Deduzidos</p>
    <p>Cada leilão irá informar o Usuário Registrado quantos créditos serão deduzidos por Licitação. O número de créditos deduzidos por Licitação varia entre 1 e 30 Créditos.</p>

    <p>Depois de uma licitação ser processada pelo Website o número de créditos é deduzido da conta do Usuário Registrado. Os créditos cobrados não podem ser devolvidos ao Usuário Registrado.</p>

    <p>O Usuário Registrado não pode escolher se quer usar Créditos grátis ou pagos (esta situação iria complicar o ato de licitar simplesmente ao clicar num botão). Os créditos são usados pela ordem em que foram adquiridos. Isso pode fazer com que o Preço Comprar Agora não desça ou desça para um valor diferente do que o esperado, quando são utilizados créditos grátis.</p>

    <p>Também não é possível licitar com um crédito específico de um determinado pacote de créditos.</p>

    <p>5.4. Dar Instruções para Licitar</p>
    <p>Existem duas opções de licitação para o Usuário Registrado em qualquer leilão listado no Website. A primeira é colocar uma Licitação Manual. A segunda é colocar uma instrução para Auto Licitação.</p>

    <p>5.4.1. Licitação Manual</p>
    <p>A Empresa oferece um botão que um Utilizador Registado deve clicar (uma vez) para colocar uma única licitação em qualquer leilão. Um Utilizador Registado também deve estar ciente de como o contador decrescente funciona se estiver a usar este método para fazer licitações. (Veja a [Seção 5.4.4] (Contador Decrescente).)</p>

    <p>5.4.2. Auto Licitação</p>
    <p>A Empresa oferece uma ferramenta para colocar licitações automaticamente nos seus leilões. Esta ferramenta permite que um Usuário Registado dê instruções para</p>

    <ul>
      <li>começar a licitar quando um preço-alvo específico é alcançado, e</li>
      <li>Licitar um determinado número de vezes.</li>
      <li>A ferramenta de Auto Licitação destina-se a ser utilizada como uma alternativa para à licitação manual. Ela liberta o Usuário Registrado da necessidade de passar longos períodos no site, mas licita como se o utilizador estivesse no Website. As instruções completas sobre a ferramenta de Auto Licitação estão disponíveis no Website.</li>
    </ul>

    <p>Um botão de Auto Licitação também é fornecido, através do qual um Usuário Registrado pode iniciar imediatamente a Auto Licitação num leilão específico, em vez da licitação manual. Este botão funciona como ferramenta de Auto Licitação até ser clicado uma segunda vez, após o que desativa a Auto Licitação.</p>

    <p>5.4.2.1. Falha na Ferramenta de Auto Licitação</p>
    <p>A Empresa isenta-se de todas e quaisquer perdas potenciais que um Usuário Registrado pode atribuir ao seu uso da ferramenta de Auto Licitação para colocar licitações, incluindo:</p>

    <ul>
      <li>Falha do Usuário Registrado em entender o uso da ferramenta,</li>
      <li>o Usuário Registrado confundir o processo de licitação pela quantidade de Créditos deduzidos, e / ou</li>
      <li>o Usuário Registrado fixar um preço-alvo mais baixo do que o preço de licitação actual.</li>
    </ul>

    <p>5.4.2.2. Atrasos nas Licitações via Ferramenta de Auto Licitação</p>

    <p>Se houver um atraso da ferramenta Auto Licitação no contato com um Usuário Registrado , isto não é accionável.</p>

    <p>5.4.2.3. Erros Técnicos</p>
    <p>Se a ferramenta Auto Licitação falhar devido a uma falha de rede, um ataque ao site ou no caso raro de erro técnico com o Website da Empresa:</p>
    <ul>
      <li>o Usuário Registrado entrará em contato com a Empresa relatando exatamente qual foi o problema; e</li>
      <li>a Empresa será obrigada, se a falha da ferramenta Auto Licitação não se dever a um fator externo, a reembolsar todos os créditos pagos gastos nesse leilão do Usuário Registado.</li>
    </ul>

    <p>5.4.2.4. Direito de Parar as Instruções para Colocar Auto Licitações</p>
    <p>A Empresa pode fechar uma instrução ativa para colocar Auto Licitações sem notificar o Usuário Registrado que colocou essas Auto Licitações.</p>

    <p>5.4.2.5. Encerramento da Auto Licitação</p>
    <p>A ferramenta de Auto Licitação vai parar de licitar para esse Usuário Registrado se um Usuário Registrado:</p>

    <ul>
