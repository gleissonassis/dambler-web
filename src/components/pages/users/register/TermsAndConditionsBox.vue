    <p>Se um Usuário Registrado permite a “Funcionalidade de Recarga Automática”, este recurso imediatamente compra um pacote de créditos de 300Créditos sempre que o saldo de créditos cai abaixo de certo nível de Créditos enquanto licita num leilão.</p>

    <p>6.9.1. Espírito e Intenção da Funcionalidade de Recarga Automática</p>
    <p>A funcionalidade de Recarga Automática garante que não há nenhuma possibilidade de um Usuário Registrado perder um leilão por ter de deixá-lo para carregar os seus créditos num momento crucial. A funcionalidade de Recarga Automática também abrange uma série de características, incluindo:</p>

