    <p>9.15.4. Produtos Retidos pela Alfândega por Pagamento Pendente de Impostos e Taxas de Importação</p>
    <p>Os produtos de um Usuário Registrado que sejam retidos pela alfândega, por qualquer motivo, não sendo da responsabilidade da Empresa, a libertação desses Produtos. O Usuário Registrado deve negociar directamente com a alfândega. A Empresa não negociará com a alfândega (em nome do Usuário Registrado) sobre os Produtos retidos, nem fará qualquer correspondência sobre as taxas que a alfândega incorreu. A obrigação da Empresa com a Alfândega é limitada à realização (ao Usuário Registrado) destas ações:</p>

    <ul>
      <li>a Empresa enviou (despachou) o Produto(s) do seu armazém;</li>
      <li>o transportador entregou o(s) Produto(s) no país de destino do Usuário Registrado;</li>
      <li>o(s) Produto(s) foram retidos pela alfândega daquele país.</li>
    </ul>

    <p>9.15.5. Produtos Devolvidos pela Alfândega por Falta de Pagamento de Impostos e Taxas de Importação</p>
    <p>Caso o Usuário Registrado não pague as taxas necessárias, para que os Produtos sejam transportados até si, a Alfândega devolverá os Produtos à Empresa. Depois disso, a Empresa avaliará o modo de compensar o Usuário Registrado , de acordo com a [Seção 9.16.6] (Compensação/Reembolso de Entrega Comprometida por Falta de Pagamento de Impostos e Taxas de Importação à Alfândega).</p>

