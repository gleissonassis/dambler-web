    <p>6.9.4. Exemplo de Operação de Recarga Automática</p>
    <p>Porque pode haver um pequeno atraso com um Processador de Pagamentos no processo, uma ordem de Recarga Automática será accionada quando o Nível de Créditos de um Usuário Registrado descer abaixo de um certo nível.</p>

    <ul>
      <li>Se estiver licitando num leilão de 2 Créditos, o nível de Créditos que desencadeia uma Recarga Automática é 20.</li>
      <li>Se estiver licitando num leilão de 4 Créditos, o nível de Créditos que desencadeia uma Recarga Automática é 40.</li>
      <li>Se estiver licitando num leilão de 6 Créditos, o nível de Créditos que desencadeia uma Recarga Automática é 60.</li>
      <li>Se estiver licitando num leilão de 8 Créditos, o nível de Créditos que desencadeia uma Recarga Automática é 80.</li>
    </ul>

    <p>Por exemplo, um Usuário Registrado compra 275 créditos (que custam R$102,99). O Usuário Registrado permite a Recarga Automática. O Usuário Registrado, em seguida, começa a licitar n um leilão de 8 Créditos. Quando o seu Nível de Créditos desce para 80 créditos ou menos, a Recarga Automática vai comprar 300 créditos adicionais por R$111,99.</p>

    <p>Esse processo permite dar ao Processador de Pagamentos tempo suficiente para processar o pedido e minimiza qualquer risco do Usuário Registrado ficar sem créditos, apesar da Recarga Automática ser ativada.</p>

