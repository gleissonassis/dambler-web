    <p>O uso da Funcionalidade de Recarga Automática por um Usuário Registrado tem, por padrão, o limite máximo de R$130 em qualquer período de 24 horas (em execução a partir de quando for ativado). Isto quer dizer que, se um Utilizador Registrado ativar a Recarga Automática às 19:00, o seu cartão será cobrado até atingir R$130 dentro das 24 horas seguintes.</p>

    <p>Um Usuário Registrado pode aumentar este limite da Função de Recarga Automática na seção A Minha MadBid se desejar.</p>

