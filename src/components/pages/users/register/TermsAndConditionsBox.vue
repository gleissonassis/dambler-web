    <p>Seção 10. Conluio</p>
    <p>10.1. Definição de Conluio</p>
    <p>Para propósitos deste Acordo, a Conluio inclui acordos e cooperação entre dois ou mais Usuários Registrados, quando têm estas atitudes:</p>
    <ul>
      <li>limitam a competição aberta para qualquer Produto ou Leilão;</li>
      <li>desencorajam outros Usuários Registrados à participação em qualquer Leilão;</li>
      <li>utilizam o Site de modo a distrai-lo do seu propósito original;</li>
      <li>usam o Site de um modo que tenha um efeito negativo na «alma» e no prazer do Site.</li>
    </ul>
    <p>A Colusão também incorpora quaisquer atividades que deem a impressão de que há mais competição (para qualquer Leilão ou Produto) do que realmente existe! Exemplos seriam:</p>
    <ul>
      <li>um grupo de Usuários Registrados que se junta para licitar no mesmo Leilão (assim, para os outros usuários registados ficará a impressão de que o artigo é muito desejado);</li>
      <li>um grupo de Usuários Registrados que concorda com a divisão da arrecadação de qualquer Leilão ganho, para depois licitarem em grupo naquele Leilão (desta forma, um membro do grupo não estaria a competir com o outro membro, mas todos estariam a competindo, apenas, com Usuários Registrados fora daquele grupo);</li>
      <li>um grupo de Usuários Registrados da mesma residência, família ou grupo de amigos que licita em conjunto no mesmo Leilão;</li>
      <li>um grupo de Usuários Registrados que coloca dinheiro num único Leilão, usando uma única identidade; e</li>
      <li>um grupo de Usuários Registrados que atacam, intencionalmente, outro único Usuários Registrados.</li>
    </ul>

    <p>10.2. Investigação de Conluio</p>
    <p>A Empresa monitora os Créditos gastos, o modo como os Usuários Registrados gastam esses Créditos e as licitações feitas por eles em todos os Leilões. A Empresa investigará qualquer dado que indique a Conluio. Os exemplos de comportamentos que acionarão uma investigação são fornecidos na [Seção 3.9] (Direito de Investigar os Leilões por Suspeita de Conluio).</p>

    <p>10.3. Evidência de Conluio</p>
    <p>A Empresa investigará ainda mais, caso encontre:</p>
    <ul>
      <li>dois ou mais Usuários Registrados que licitam no mesmo Leilão usando o mesmo endereço de IP;</li>
      <li>dois ou mais Usuários Registrados com o mesmo endereço ou Identificador de Pagamentos.;</li>
      <li>dois ou mais Usuários Registrados que licitam no mesmo Leilão e partilham características em comum, por exemplo, nomes de utilizador, data de nascimento e localidades geográficas similares;</li>
      <li>padrões entre dois ou mais Usuários Registrados que licitam num Leilão, mas somente quando outros Usuários Registrados não estejam a licitando naquele leilão!</li>
    </ul>

    <p>Esta lista não é exaustiva.</p>

    <p>10.4. Recolha de Provas Requeridas para Conluio</p>
    <p>A Empresa baseará a sua conclusão numa interpretação razoável (levando em conta a evidência). Caso a investigação da Empresa conclua que há evidência de Conluio entre vários Usuários Registrados, a Empresa informará o Usuário Registrado suspeito de Conluio sobre o motivo da sua suspeita.</p>

    <p>10.5. Contestar a Alegação de Conluio da Empresa</p>
    <p>Qualquer disputa (por um Usuário Registrado) sobre a legitimidade da decisão da Empresa, quando afirmou que houve conluio, deve ser feita por escrito.</p>

    <p>O Usuário Registrado poderá acessar à evidência necessária (com base na qual a Empresa tomou a decisão), de acordo com a legislação de proteção de dados existente, mas terá de pagar uma taxa (atualmente R$35) de custos administrativos.</p>

    <p>Caso o Usuário Registrado acredite que a Empresa tenha suspeitado indevidamente de Conluio, baseada na evidência disponível, nenhuma parte destes Termos e Condições interferirá com o seu direito de iniciar uma ação legal contra a Empresa. No entanto, o Usuário Registrado estará restrito ao requerimento pelo custo dos Créditos Pagos que gastou, pessoalmente, no Leilão.</p>

    <p>10.6. Alegações Contestadas</p>
    <p>Nada na [Seção 10.5] (Contestar a Alegação de Conluio da Empresa) afetará ou atrasará o direito da Empresa reiniciar qualquer Leilão, sob a [Seção 3.10] (Direito de Recomeçar Leilões em caso de Conluio) e [Seção 3.17] (Direito de Reiniciar Leilões em caso de Ataque ao Site). Caso um Usuário Registrado conteste a alegação da Empresa sobre o conluio (em relação a um Leilão), a Empresa retém o direito de reiniciar o Leilão, mesmo com a disputa. O procedimento correto do Usuário Registrado, nessas circunstâncias, é seguir as instruções da [Seção 10.5] (Contestar a Alegação de Conluio da Empresa).</p>

    <p>10.7. Efeito de Conluio</p>
    <p>No seguimento de qualquer investigação sobre Conluio, a Empresa pode chegar a uma destas conclusões:</p>

    <ul>
      <li>Sem evidências de Conluio;</li>
      <li>Evidência razoável de Conluio; ou</li>
      <li>Suspeita de Conluio.</li>
    </ul>

    <p>10.7.1. Sem Evidência de Conluio</p>
    <p>Nenhuma ação será requerida, e a Empresa reiniciará o Leilão.</p>

    <p>10.7.2. Evidência Razoável de Conluio</p>
    <p>Para que a Empresa tenha uma evidência razoável de Conluio(baseada numa crença razoável e honesta, tendo considerado a evidência pertinente):</p>
    <ul>
      <li>o critério de Conluio (definida na [Seção 10.1] (Definição de Conluio) foi satisfeito;</li>
      <li>a evidência de Conluio (definida na [Seção 10.2] (Investigação da Conluio) é muito significativa, e qualquer pessoa racional iria concluir que a Conluio aconteceu.</li>
      <li>Não é necessário que a Conluio resulte na vitória no Leilão por parte dos Usuários Registrados que praticaram a Conluio.</li>
    </ul>

    <p>Caso os números 1 e 2 sejam satisfeitos, a Empresa poderá tomar as seguintes medidas:</p>

    <ul>
      <li>a Empresa suspenderá todos os Usuários culpados de Conluio;</li>
      <li>a Empresa não será obrigada a aceitar a Licitação Vencedora no Leilão em que houve a Conluio;</li>
      <li>a Empresa colocará aquele Leilão de volta no estado anterior à Conluio;</li>
      <li>a Empresa reiniciará o Leilão.</li>
    </ul>

