    <p>9.13. Produtos não Entregues ou Reclamados</p>
    <p>Empresas diferentes operam com políticas diferentes, quando relacionadas à entrega! Se o receptor de um Produto estiver fora da sua residência, dependendo do tipo de transportadora usada, uma destas coisas poderá acontecer:</p>

    <ul>
