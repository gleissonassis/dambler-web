    <p>Nem todos os Produtos estarão disponíveis na loja de Desconto Ganho. A falta de estoque pode significar que um Produto foi removido da loja de Desconto Ganho, temporariamente, permanentemente ou até novos estoques chegarem. Outros Produtos podem nunca ficar disponíveis na loja do Desconto Ganho. Nenhuma proteção é dada ou inferida na disponibilidade da loja do Desconto Ganho, para qualquer Produto leiloado no Website. No entanto, o Website oferece uma gama de Produtos. Caso um Produto não esteja disponível, outro Produto (com especificações técnicas similares) estará!</p>

