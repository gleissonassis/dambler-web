    <p>Cada vez que um Usuário Registrado coloca uma licitação, não importa o número de créditos que são deduzidos da conta do Usuário Registrado, o preço do leilão vai aumentar em R$0,01 (um centavo).</p>

    <p>5.4.7. Vencedor do Leilão</p>
    <p>O último Usuário Registrado a colocar uma Licitação antes de o Contador Decrescente chegar a zero é considerado o vencedor do leilão. Ele será capaz de se oferecer para comprar o produto ao preço de fecho mais despesas de envio.</p>

    <p>5.4.7.1. Disputas com o Vencedor do Leilão</p>
    <p>O vencedor do leilão é determinado pelos registos da Empresa. No caso raro de qualquer disputa que ocorra, é a empresa que decide quem colocou a última licitação no leilão. A decisão da companhia será final.</p>

    <p>5.4.8. Contrato de Compra e Venda do Produto</p>
    <p>Ao colocar uma licitação num leilão, um Usuário Registrado concorda em comprar o produto em destaque se tiver a Licitação vencedora nesse leilão. Além disso, compromete-se a pagar a Licitação vencedora (mais despesas de envio) dentro do prazo para pagamento da Licitação vencedora. Se o Usuário Registrado não fizer o pagamento dentro desse prazo, então:</p>

    <ul>
      <li>O Usuário Registrado perde o direito a comprar o produto, e</li>
      <li>A Empresa tem o direito de reter a propriedade do produto.</li>
    </ul>

