    <p>5.4.4. Contador Decrescente</p>
    <p>Todos os Leilões exibem um Contador e um Contador Decrescente que pode ser encontrado na aba "Informações do Leilão". O Contador Decrescente não é constante. Normalmente, o Contador Decrescente é reduzido durante todo o processo de leilão, devido a um aumento de interesse dos Usuários Registrados. O Contador Decrescente não é aumentado a menos que seja exigido pelas condições em casos excepcionais, por exemplo, DDOS (Negação de serviço Distribuído) ataques e / ou outras questões técnicas.</p>

    <p>O Contador exibido é uma aproximação visual do Contador do Servidor. O Contador exibido é tipicamente preciso para o Usuário Registrado. No entanto, atrasos na rede, as diferenças de navegador e problemas de conexão podem causar um atraso entre o Contador exibido e o Contador do Servidor. Isto significa que os Usuários Registrados nunca devem esperar até ao último segundo para colocar uma licitação, porque o Contador usado pelo sistema é o Contador do servidor. Quando o Contador do servidor atinge 0, o Usuário Registrado que colocou a última licitação aceite ganha o leilão e pode fazer o pagamento do produto.</p>

    <p>5.4.5. Reinício do Contador Decrescente</p>
    <p>Cada vez que um Usuário Registrado coloca uma licitação, o contador será reiniciado a partir do Contador Decrescente exibido conforme a [Seção 5.4.4] (Contador Decrescente).</p>

