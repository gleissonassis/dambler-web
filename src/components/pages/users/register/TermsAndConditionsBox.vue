    <p>9.13.2. Produtos Deixados com o Vizinho</p>
    <p>A Empresa usa parceiros de entrega confiáveis e considera como sua obrigação a gestão da entrega até o endereço do Cliente. As transportadoras só deixam as mercadorias com um vizinho para acelerar o processo de recolha e sempre pedirão que o vizinho aceite a entrega em nome do Cliente. Em raras situações, o vizinho recusa-se a fazer a entrega ao Cliente. Ações assim, evidentemente, estão fora do controlo da Empresa!</p>

    <p>Todos os Usuários Registados são encorajados a não receber a entrega num endereço onde os vizinhos sejam de carácter duvidoso.</p>

    <p>A Empresa não atenderá a quaisquer reclamações, que exijam que a Empresa forneça outro Produto, caso um vizinho tenha tomado a posse do mesmo. A Empresa é obrigada a aceitar os termos e condições da Transportadora, incluindo os seus termos de entrega, já que permitem deixar as entregas com os vizinhos, da mesma forma como qualquer outro consumidor aceitaria.</p>

    <p>Nesses casos, o receptor deve relatar o roubo à polícia e tem de prestar uma queixa civil contra o vizinho. A Empresa irá, então, ajudar, fornecendo todas as informações de entrega ao receptor.</p>

    <p>9.14. Casos de Entrega Alterada pelo Usuário Registrado</p>
    <p>A Empresa usará sempre uma transportadora confiável para fazer a entrega de qualquer Produto a um Usuário Registrado. A Empresa já percebeu, particularmente no que diz respeito a entregas internacionais, que, às vezes, as transportadoras domésticas podem ter recebido instruções prévias (de um Usuário Registrado) sobre a forma como a Entrega deve acontecer na sua residência. Por exemplo, o serviço de correios da Alemanha permite que um Usuário Registrado possa pedir, caso não esteja em casa (no momento da Entrega), que a Entrega seja deixada «estacionada» no perímetro da sua residência.</p>

    <p>Para acabar com todas as dúvidas, a Empresa considera como sua obrigação a entrega de um Produto, mas age como a fonte, a instrutora e a pagadora de uma transportadora confiável que leve qualquer Produto vencido ou comprado ao endereço fornecido pelo Usuário Registrado. Caso o Usuário Registrado não receba o Produto, a Empresa considerará que o Produto foi perdido no trajecto, sob a [Seção 9.12] (Produtos Perdidos no Trajecto). No entanto, caso as acções de um Usuário Registrado tenham alterado a Entrega (o que significa que a transportadorar foi instruída pelo Usuário Registrado a fazer a entrega de modo diferente), a Empresa não será responsável pelo destino dessa Entrega.</p>

    <p>9.15. Produtos com Taxas Alfandegárias e Outras Taxas de Entrada num País</p>
    <p>Um Usuário Registrado que vença um Leilão ou que use o Comprar Agora deve pagar um preço (referente ao Produto) que inclua a entrega para o país de destino!</p>

