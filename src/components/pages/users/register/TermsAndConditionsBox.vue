    <p>A Empresa lista alguns Leilões que aparecem em vários países. Todos os leilões que são Leilões internacionais estão claramente identificados como tal. Os detalhes de um leilão internacional podem variar de país para país da seguinte forma:</p>

    <ul>
      <li>pela língua natural - a descrição do leilão e do produto estará na língua natural do Website visualizado, e</li>
      <li>pela Especificação do produto - pode haver diferenças entre a especificação de um produto individual.</li>
